#enter a whole number and you will get the area and circumfrence of a circle with that radius
PI = 3.14159
circleRad = int(input("Enter the radius of your circle: "))
print(" ")
circleArea = PI * (circleRad * circleRad)
circleCir = PI * circleRad * 2
print("A circle with ", circleRad, " Radius, has an area of ", circleArea)
print("and a circumfrence of ", circleCir)
